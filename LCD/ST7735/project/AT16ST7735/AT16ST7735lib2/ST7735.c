 //=======================================запись байта=============================================
 void write_byte(unsigned char c,unsigned char com)//0-command 1-data
 {
	PORT_LCD&=~(1<<CS);
	 if(com==0)
	 {
	 PORT_LCD&=~(1<<RS);
	 }
	 else
	 {
	 PORT_LCD|=(1<<RS);
	 }
	 PORT_LCD|=(1<<SCK);
	 for(unsigned char i=0;i<8;i++)
	 {
		 if(((c>>(7-i))&1) == 1)
		 {
		 PORT_LCD|=(1<<MOSI);
		 }
		 else
		 {
		 PORT_LCD&=~(1<<MOSI);

		 }
		 PORT_LCD&=~(1<<SCK);
		PORT_LCD|=(1<<SCK);
		 
	 }
	 PORT_LCD|=(1<<CS);
 }
 //===========================================Сброс=====================================================
 void Reset()
 {
	PORT_LCD&=~(1<<RST);
	 _delay_ms(1);
	 PORT_LCD|=(1<<RST);
	 _delay_ms(1);

 }
